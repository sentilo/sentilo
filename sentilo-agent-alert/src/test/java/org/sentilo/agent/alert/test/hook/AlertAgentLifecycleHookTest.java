package org.sentilo.agent.alert.test.hook;

import static org.mockito.Matchers.any;
import static org.mockito.Matchers.anyString;
import static org.mockito.Mockito.times;
import static org.mockito.Mockito.verify;
import static org.mockito.Mockito.when;

import org.junit.Assert;
import org.junit.Before;
import org.junit.BeforeClass;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.MockitoAnnotations;
import org.powermock.api.mockito.PowerMockito;
import org.powermock.core.classloader.annotations.PrepareForTest;
import org.powermock.modules.junit4.PowerMockRunner;
import org.sentilo.agent.alert.hook.AlertAgentLifecycleHook;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.data.redis.listener.RedisMessageListenerContainer;
import org.springframework.test.util.ReflectionTestUtils;

@RunWith(PowerMockRunner.class)
@PrepareForTest({AlertAgentLifecycleHook.class, LoggerFactory.class, ReflectionTestUtils.class})
public class AlertAgentLifecycleHookTest {

  @InjectMocks
  private AlertAgentLifecycleHook hook;

  @Mock
  private RedisMessageListenerContainer listenerContainer;

  private static Logger logger;

  @BeforeClass
  public static void setUpStatic() throws Exception {
    PowerMockito.mockStatic(LoggerFactory.class);
    logger = PowerMockito.mock(Logger.class);
    when(LoggerFactory.getLogger(anyString())).thenReturn(logger);
    when(LoggerFactory.getLogger(any(Class.class))).thenReturn(logger);

  }

  @Before
  public void setUp() {
    MockitoAnnotations.initMocks(this);
    hook.start();
  }

  @Test
  public void doGracefulShutdown() {
    hook.stop();
    Assert.assertFalse(hook.isRunning());
    verify(logger, times(2)).debug(anyString());
  }

}
